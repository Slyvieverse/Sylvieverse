    <script>
        // Animate stats counting
        document.addEventListener('DOMContentLoaded', function() {
            const stats = [
                { element: 'collectors-count', target: 12457, suffix: '+' },
                { element: 'assets-count', target: 2834, suffix: '+' },
                { element: 'volume-count', target: 4852, suffix: '+' },
                { element: 'chains-count', target: 3, suffix: '' }
            ];

            stats.forEach(stat => {
                const element = document.getElementById(stat.element);
                if (element) {
                    animateCounter(element, stat.target, stat.suffix);
                }
            });

            function animateCounter(element, target, suffix) {
                let current = 0;
                const increment = target / 100;
                const timer = setInterval(() => {
                    current += increment;
                    if (current >= target) {
                        element.textContent = target.toLocaleString() + suffix;
                        clearInterval(timer);
                    } else {
                        element.textContent = Math.floor(current).toLocaleString() + suffix;
                    }
                }, 20);
            }

            // Add intersection observer for animation triggers
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('animate-in');
                    }
                });
            }, { threshold: 0.1 });

            // Observe stat items for animation
            document.querySelectorAll('.stat-item').forEach(item => {
                observer.observe(item);
            });
        });
    </script>
